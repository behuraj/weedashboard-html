    <!-- Entrepreneurship Charts Script -->
    <script>
        // Metric values mapping
        const metricValues = {
            'entrepreneurship-chart-1': 65,  // WEE Index
            'entrepreneurship-chart-2': 94,  // Employment
            'entrepreneurship-chart-3': 96,  // Education & Skilling
            'entrepreneurship-chart-4': 66,  // Livelihood
            'entrepreneurship-chart-5': 77,  // Mobility, Safety & Inclusive Infra (default value)
            'entrepreneurship-chart-6': 87   // Entrepreneurship
        };

        // Generate year data for 2021, 2022, 2023, 2024 with specific metric value
        function generateYearData(metricValue) {
            const years = ['2021', '2022', '2023', '2024'];
            
            // Distribute the metric value across 4 years (each year gets the same value)
            const yearData = years.map(year => ({
                year: year,
                value: metricValue
            }));
            
            return {
                years: yearData.map(item => item.year),
                data: yearData.map(item => item.value)
            };
        }

        // Function to create Circle Chart - Custom Angle (matching charts-apex-radialbar.php)
        function createEntrepreneurshipChart(chartId) {
            const metricValue = metricValues[chartId] || 50; // Default to 50 if not found
            const chartData = generateYearData(metricValue);
            var colors = ['#0acf97', '#777edd', '#fa5c7c', '#f9bc0b'];
            var dataColors = $("#" + chartId).data('colors');
            if (dataColors) {
                colors = dataColors.split(",");
            }

            var options = {
                chart: {
                    height: 380,
                    type: 'radialBar',
                },
                plotOptions: {
                    radialBar: {
                        offsetY: -30,
                        startAngle: 0,
                        endAngle: 270,
                        hollow: {
                            margin: 5,
                            size: '30%',
                            background: 'transparent',
                            image: undefined,
                        },
                        track: {
                            background: "rgba(170,184,197, 0.2)"
                        },
                        dataLabels: {
                            name: {
                                show: false,
                            },
                            value: {
                                show: false,
                            }
                        }
                    }
                },
                colors: colors,
                series: chartData.data,
                labels: chartData.years,
                legend: {
                    show: true,
                    floating: true,
                    fontSize: '13px',
                    position: 'left',
                    offsetX: 10,
                    offsetY: 10,
                    labels: {
                        useSeriesColors: true,
                    },
                    markers: {
                        size: 0
                    },
                    formatter: function (seriesName, opts) {
                        return seriesName + ":  " + opts.w.globals.series[opts.seriesIndex] + "%"
                    },
                    itemMargin: {
                        horizontal: 1,
                    }
                },
                responsive: [{
                    breakpoint: 480,
                    options: {
                        legend: {
                            show: false
                        }
                    }
                }]
            };

            var chart = new ApexCharts(
                document.querySelector("#" + chartId),
                options
            );
            chart.render();
        }

        // Initialize all 6 charts
        for (let i = 1; i <= 6; i++) {
            createEntrepreneurshipChart('entrepreneurship-chart-' + i);
        }
    </script>
